# This helper function will take a list of models and a dataloader,
# and return the predictions (probabilities) and true labels.
def get_oof_predictions(dataloader, models):
    # Deactivate gradients for performance
    with torch.no_grad():
        all_labels = []
        # Store predictions from each model in a separate list
        model_predictions = [[] for _ in models]

        for inputs, labels in tqdm(dataloader, desc="Generating Meta-Features"):
            inputs = inputs.to(device)
            all_labels.extend(labels.cpu().numpy())

            for i, model in enumerate(models):
                outputs = model(inputs)
                # We use softmax to get probabilities, which are more informative
                # for the meta-learner than simple class predictions.
                probabilities = F.softmax(outputs, dim=1)
                model_predictions[i].extend(probabilities.cpu().numpy())

    # Concatenate predictions from all models horizontally
    # From [[model1_preds], [model2_preds], ...] to a single numpy array
    meta_features = np.concatenate([np.array(p) for p in model_predictions], axis=1)
    return meta_features, np.array(all_labels)

