# --- Specify the model to train and fine-tuning parameters ---
model_name = 'resnet50'
WARMUP_EPOCHS = 5
FINETUNE_EPOCHS = 25
TOTAL_EPOCHS = WARMUP_EPOCHS + FINETUNE_EPOCHS

# Learning Rates
# Use a standard LR for the head during warmup
LR_HEAD_WARMUP = 1e-3
# Use a very low LR for the unfrozen backbone layers
LR_BACKBONE_FINETUNE = 1e-5
# Use a low LR for the head during fine-tuning
LR_HEAD_FINETUNE = 1e-4


print(f"\n{'='*35}\n   Fine-Tuning Model: {model_name.upper()}\n{'='*35}")

# 1. Load the initial model (all base layers are frozen by get_model)
# (This requires get_model, num_classes, and device to be defined)
model = get_model(model_name, num_classes).to(device)
criterion = nn.CrossEntropyLoss()

# To keep track of the best model during training
best_val_acc = 0.0
best_model_wts = copy.deepcopy(model.state_dict())

# ==============================================================================
# STAGE 1: WARMUP - Train only the classifier head
# ==============================================================================
print(f"\n--- STAGE 1: WARMUP ({WARMUP_EPOCHS} Epochs) ---")
print("Training only the final classifier layer...")

# Optimizer for the head only
optimizer_head = optim.Adam(model.fc.parameters(), lr=LR_HEAD_WARMUP)

for epoch in range(WARMUP_EPOCHS):
    # --- Training Step ---
    model.train()
    for inputs, labels in tqdm(train_loader, desc=f"Warmup Epoch {epoch+1}/{WARMUP_EPOCHS} [Train]"):
        inputs, labels = inputs.to(device), labels.to(device)
        optimizer_head.zero_grad()
        outputs = model(inputs)
        loss = criterion(outputs, labels)
        loss.backward()
        optimizer_head.step()

    # --- Validation Step ---
    model.eval()
    val_loss = 0.0
    val_corrects = 0
    with torch.no_grad():
        for inputs, labels in tqdm(val_loader, desc=f"Warmup Epoch {epoch+1}/{WARMUP_EPOCHS} [Val]"):
            inputs, labels = inputs.to(device), labels.to(device)
            outputs = model(inputs)
            _, preds = torch.max(outputs, 1)
            val_loss += criterion(outputs, labels).item() * inputs.size(0)
            val_corrects += torch.sum(preds == labels.data)

    epoch_acc = val_corrects.double() / len(val_dataset)
    print(f"Validation Accuracy: {epoch_acc:.4f}")

    if epoch_acc > best_val_acc:
        best_val_acc = epoch_acc
        best_model_wts = copy.deepcopy(model.state_dict())
        print("✅ New best model saved!")

print("\n✅ Warmup complete.")


# ==============================================================================
# STAGE 2: UNFREEZE & FINE-TUNE
# ==============================================================================
print(f"\n--- STAGE 2: FINE-TUNING ({FINETUNE_EPOCHS} Epochs) ---")
print("Unfreezing the top layers (layer4) of the model...")

# Unfreeze the parameters of the last convolutional block
for param in model.layer4.parameters():
    param.requires_grad = True

# Create a new optimizer with different learning rates for different parts of the model
# This is the key to successful fine-tuning.
optimizer_finetune = optim.Adam([
    {'params': model.layer4.parameters(), 'lr': LR_BACKBONE_FINETUNE},
    {'params': model.fc.parameters(), 'lr': LR_HEAD_FINETUNE}
])

print("Starting full model fine-tuning...")
for epoch in range(FINETUNE_EPOCHS):
    # --- Training Step ---
    model.train()
    for inputs, labels in tqdm(train_loader, desc=f"Finetune Epoch {epoch+1}/{FINETUNE_EPOCHS} [Train]"):
        inputs, labels = inputs.to(device), labels.to(device)
        optimizer_finetune.zero_grad()
        outputs = model(inputs)
        loss = criterion(outputs, labels)
        loss.backward()
