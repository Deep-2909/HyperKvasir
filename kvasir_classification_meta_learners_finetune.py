# 3. Ensemble Prediction Function (Soft Voting)
def ensemble_predict(models, dataloader, device):
    all_preds, all_labels = [], []

    with torch.no_grad():
        for inputs, labels in tqdm(dataloader, desc="Ensemble Inference"):
            inputs, labels = inputs.to(device), labels.to(device)

            # Collect probabilities from each model
            probs = []
            for model in models:
                outputs = model(inputs)
                probs.append(F.softmax(outputs, dim=1))  # convert to probabilities

            # Average probabilities
            avg_probs = torch.mean(torch.stack(probs), dim=0)

            # Get final predictions
            _, preds = torch.max(avg_probs, 1)

            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())

    return np.array(all_labels), np.array(all_preds)

