# --- 2. UPDATED MODEL DEFINITION HELPER ---
def get_model(model_name, num_classes, pretrained=True):
    model = None
    weights = 'DEFAULT' if pretrained else None

    # Load the base model
    if model_name == 'resnet50':
        model = models.resnet50(weights=weights)
    elif model_name == 'vgg16':
        model = models.vgg16(weights=weights)
    elif model_name == 'mobilenet_v2':
        model = models.mobilenet_v2(weights=weights)
    elif model_name == 'densenet121':
        model = models.densenet121(weights=weights)
    elif model_name == 'efficientnet_b6':
        model = models.efficientnet_b6(weights=weights)
    else:
        raise ValueError("Model name not recognized.")

    # Freeze all the base model parameters
    for param in model.parameters():
        param.requires_grad = False

    # --- UPDATED SECTION TO CORRECTLY REPLACE THE FINAL LAYER ---
    if model_name == 'resnet50':
        num_ftrs = model.fc.in_features
        model.fc = nn.Linear(num_ftrs, num_classes)

    elif model_name == 'vgg16':
        # VGG's classifier is a sequence. The last layer is at index 6.
        num_ftrs = model.classifier[6].in_features
        model.classifier[6] = nn.Linear(num_ftrs, num_classes)

    elif model_name == 'mobilenet_v2':
        # MobileNet's classifier is a sequence. The last layer is at index 1.
        num_ftrs = model.classifier[1].in_features
        model.classifier[1] = nn.Linear(num_ftrs, num_classes)

    elif model_name == 'densenet121':
        # DenseNet's classifier is a single layer, so the original code works.
        num_ftrs = model.classifier.in_features
        model.classifier = nn.Linear(num_ftrs, num_classes)

    elif model_name == 'efficientnet_b6':
        # EfficientNet's classifier is a sequence. The last layer is at index 1.
        num_ftrs = model.classifier[1].in_features
        model.classifier[1] = nn.Linear(num_ftrs, num_classes)

    return model

